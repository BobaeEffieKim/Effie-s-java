package practice06_14_getter_setter;

//Source에 있는 getter&setter 자동생성기능 해보기

public class Car2 {

	private int speed1;
	private boolean stop1;
	
	
	
	
	public int getSpeed1() {
		return speed1;
	}
	public void setSpeed1(int speed1) {
		this.speed1 = speed1;
	}
	public boolean isStop1() {
		return stop1;
	}
	public void setStop1(boolean stop1) {
		this.stop1 = stop1;
	}
	
}
